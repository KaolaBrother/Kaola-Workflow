#!/usr/bin/env node
'use strict';
const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');
const { issueIsClosed } = require('./kaola-workflow-active-folders');

function assert(cond, msg) { if (!cond) throw new Error(msg); }

function field(content, name) {
  const escaped = name.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const m = content.match(new RegExp('^' + escaped + ':[ \\t]*(.+)$', 'm'));
  return m ? m[1].trim() : '';
}

function getRoot() {
  try {
    return execFileSync('git', ['rev-parse', '--show-toplevel'], {
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore']
    }).trim();
  } catch (_) {
    return process.cwd();
  }
}

function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i++) {
    if (argv[i].startsWith('--') && argv[i + 1] !== undefined && !argv[i + 1].startsWith('--')) {
      args[argv[i].slice(2)] = argv[++i];
    }
  }
  return args;
}

const RULES_BLOCK = `
## Rules

- A separate roadmap/research session owns discovering and adding future work to GitHub issues.
- \`workflow-next\` fetches GitHub issues, mirrors active implementation work here, and advances one item per cycle.
- After each workflow cycle, refresh this file from issue state.
- Move completed workflow project folders to \`kaola-workflow/archive/\`.
- Close linked GitHub issues only after acceptance criteria pass.`;

const HEADER = '<!-- generated by scripts/kaola-workflow-roadmap.js — do not edit -->\n' +
  '# Kaola-Workflow Roadmap\n\n' +
  'This file mirrors active unfinished work. GitHub issues are the source of truth when available.\n\n' +
  '## Active Work\n\n' +
  '| Issue | Title | Status | Workflow Project | Next Step |\n' +
  '|-------|-------|--------|------------------|-----------|';

function roadmapDir(root) {
  return path.join(root, 'kaola-workflow', '.roadmap');
}

function roadmapFile(root) {
  return path.join(root, 'kaola-workflow', 'ROADMAP.md');
}

function readRoadmapIssues(dir) {
  if (!fs.existsSync(dir)) return [];
  const files = fs.readdirSync(dir)
    .filter(f => /^issue-\d+\.md$/.test(f))
    .sort((a, b) => {
      const na = parseInt(a.match(/\d+/)[0], 10);
      const nb = parseInt(b.match(/\d+/)[0], 10);
      return na - nb; // ascending workflow order
    });
  return files.map(f => {
    const content = fs.readFileSync(path.join(dir, f), 'utf8');
    const n = parseInt(f.match(/\d+/)[0], 10);   // filename is authority
    return {
      issue: '#' + n,
      title: field(content, 'title') || '—',
      status: field(content, 'status') || 'open',
      workflow_project: field(content, 'workflow_project') || '—',
      next_step: field(content, 'next_step') || '—',
    };
  });
}

function buildTableRow(data) {
  const cols = [
    data.issue,
    (data.title || '—').replace(/\|/g, '\\|'),
    data.status || 'open',
    (data.workflow_project || '—').replace(/\|/g, '\\|'),
    (data.next_step || '—').replace(/\|/g, '\\|'),
  ];
  return '| ' + cols.join(' | ') + ' |';
}

function buildRoadmapContent(issues, dir) {
  const rows = issues.length > 0
    ? issues.map(buildTableRow)
    : ['| none | No active work | — | — | — |'];
  let rules = RULES_BLOCK;
  if (dir) {
    const projRules = path.join(dir, '_rules.md');
    if (fs.existsSync(projRules)) {
      const extra = fs.readFileSync(projRules, 'utf8').trim();
      if (extra) rules += '\n\n### Project rules\n' + extra;
    }
  }
  return HEADER + '\n' + rows.join('\n') + '\n' + rules + '\n';
}

function isGeneratedRoadmap(content) {
  return content.startsWith('<!-- generated by scripts/kaola-workflow-roadmap.js');
}

function guardAgainstMissingRoadmapSource(dir, outFile) {
  if (!fs.existsSync(outFile)) return;
  const existing = fs.readFileSync(outFile, 'utf8');
  if (!isGeneratedRoadmap(existing)) return;
  const activeRows = parseRoadmapTable(existing);
  if (activeRows.length === 0) return;
  const sourceCount = readRoadmapIssues(dir).length;
  if (sourceCount > 0) return;
  throw new Error(
    'Refusing to replace non-empty generated ROADMAP.md with no active work because ' +
    'kaola-workflow/.roadmap is missing or contains no issue-*.md source files. ' +
    'Restore .roadmap or recreate per-issue source files before generate.'
  );
}

function writeFileAtomicReplace(filePath, content) {
  let existing = '';
  try { existing = fs.readFileSync(filePath, 'utf8'); } catch (_) {}
  if (existing === content) return false;
  const dir = path.dirname(filePath);
  fs.mkdirSync(dir, { recursive: true });
  const tmp = path.join(
    dir,
    '.' + path.basename(filePath) + '.' + process.pid + '.' + Date.now() + '.' +
      Math.random().toString(16).slice(2) + '.tmp'
  );
  let fd;
  try {
    fd = fs.openSync(tmp, 'wx');
    fs.writeFileSync(fd, content, 'utf8');
    fs.fsyncSync(fd);
    fs.closeSync(fd);
    fd = undefined;
    fs.renameSync(tmp, filePath);
  } catch (err) {
    if (fd !== undefined) {
      try { fs.closeSync(fd); } catch (_) {}
    }
    try { fs.unlinkSync(tmp); } catch (_) {}
    throw err;
  }
  // #685 (R17): fsync the PARENT DIRECTORY after the rename settles — on POSIX filesystems a rename's
  // directory-entry update is not itself durable until the containing directory is fsynced, so without
  // this a settled write can still revert to the pre-rename entry after power loss even though the tmp
  // file's own contents were fsynced above. Node has no dedicated "fsync a directory" API, so this opens
  // the directory read-only, fsyncs that fd, and closes it. Platform fail-soft is a HARD requirement:
  // some platforms/filesystems refuse to open or fsync a directory (Windows, EISDIR/EACCES/EINVAL) —
  // degrade silently to the pre-#685 behavior rather than turning a previously-accepted write into a
  // refusal; nothing in this block may rethrow or affect the return value.
  let dirFd;
  try {
    dirFd = fs.openSync(dir, 'r');
    fs.fsyncSync(dirFd);
  } catch (_) {
    // fail-soft: directory fsync unsupported/denied here — the rename above already succeeded.
  } finally {
    if (dirFd !== undefined) { try { fs.closeSync(dirFd); } catch (_) {} }
  }
  return true;
}

function createFileExclusive(filePath, content) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  let fd;
  let created = false;
  try {
    fd = fs.openSync(filePath, 'wx');
    created = true;
    fs.writeFileSync(fd, content, 'utf8');
    fs.fsyncSync(fd);
    fs.closeSync(fd);
    fd = undefined;
    return true;
  } catch (err) {
    if (fd !== undefined) {
      try { fs.closeSync(fd); } catch (_) {}
    }
    if (err && err.code === 'EEXIST') return false;
    if (created) {
      try { fs.unlinkSync(filePath); } catch (_) {}
    }
    throw err;
  }
}

function parseRoadmapTable(text) {
  // Extract only the ## Active Work section
  const m = text.match(/## Active Work\n[\s\S]*?(?=\n## |\n$|$)/);
  if (!m) return [];
  const rows = [];
  const re = /^\| #(\d+) \| ((?:[^|\\]|\\.)+?) \| ((?:[^|\\]|\\.)+?) \| ((?:[^|\\]|\\.)+?) \| ((?:[^|\\]|\\.)+?) \|$/gm;
  let match;
  while ((match = re.exec(m[0])) !== null) {
    rows.push({
      issue: '#' + match[1].trim(),
      title: match[2].trim().replace(/\\\|/g, '|'),
      status: match[3].trim(),
      workflow_project: match[4].trim().replace(/\\\|/g, '|'),
      next_step: match[5].trim().replace(/\\\|/g, '|'),
    });
  }
  return rows;
}

function regenerateRoadmap(root) {
  const repoRoot = root || getRoot();
  const dir = roadmapDir(repoRoot);
  const outFile = roadmapFile(repoRoot);
  // Narrow guard: refuse only when .roadmap/ dir is MISSING (not present-but-empty).
  // An empty-but-present dir is the legitimate close-last-issue state and must still
  // empty the mirror cleanly. Missing dir + active mirror rows = likely source loss.
  if (!fs.existsSync(dir) && fs.existsSync(outFile)) {
    const existing = fs.readFileSync(outFile, 'utf8');
    if (isGeneratedRoadmap(existing) && parseRoadmapTable(existing).length > 0) {
      throw new Error(
        'Refusing to replace non-empty generated ROADMAP.md with no active work because ' +
        'kaola-workflow/.roadmap is missing or contains no issue-*.md source files. ' +
        'Restore .roadmap or recreate per-issue source files before generate.'
      );
    }
  }
  const issues = readRoadmapIssues(dir);
  const content = buildRoadmapContent(issues, dir);
  const wrote = writeFileAtomicReplace(outFile, content);
  return wrote ? 'generated' : 'up-to-date';
}

function cmdGenerate() {
  const root = getRoot();
  // #554: rely on regenerateRoadmap's OWN narrow guard (refuses only when .roadmap/ is MISSING) as the
  // single source of truth. The redundant guardAgainstMissingRoadmapSource pre-check keyed on source COUNT
  // (==0 for a present-but-empty .roadmap/), so it WRONGLY refused the legitimate close-last-issue state
  // (empty-but-present dir) that regenerateRoadmap explicitly sanctions. The function is KEPT (two contract
  // validators pin its name + message tokens); only this redundant call is dropped.
  process.stdout.write(regenerateRoadmap(root) + '\n');
}

function cmdMigrate() {
  const root = getRoot();
  const outFile = roadmapFile(root);
  assert(fs.existsSync(outFile), 'ROADMAP.md not found at ' + outFile);
  const text = fs.readFileSync(outFile, 'utf8');
  const rows = parseRoadmapTable(text);
  const dir = roadmapDir(root);
  for (const row of rows) {
    const n = parseInt(row.issue.replace('#', ''), 10);
    if (!n || n <= 0) continue;
    const filePath = path.join(dir, 'issue-' + n + '.md');
    const content = [
      'issue: ' + row.issue,
      'title: ' + row.title.replace(/[\r\n]/g, ' '),
      'status: ' + row.status.replace(/[\r\n]/g, ' '),
      'workflow_project: ' + row.workflow_project.replace(/[\r\n]/g, ' '),
      'next_step: ' + row.next_step.replace(/[\r\n]/g, ' '),
      '',
    ].join('\n');
    if (!createFileExclusive(filePath, content)) {
      process.stdout.write('skip: issue-' + n + '.md already exists\n');
      continue;
    }
    process.stdout.write('created: issue-' + n + '.md\n');
  }
}

function cmdValidate() {
  const root = getRoot();
  const dir = roadmapDir(root);
  const outFile = roadmapFile(root);
  const issues = readRoadmapIssues(dir);
  const expected = buildRoadmapContent(issues, dir);
  let actual = '';
  try { actual = fs.readFileSync(outFile, 'utf8'); } catch (_) {}
  if (actual !== expected) {
    process.stderr.write('ROADMAP.md is stale; run: node scripts/kaola-workflow-roadmap.js generate\n');
    process.exitCode = 1;
  } else {
    process.stdout.write('ok\n');
  }
}

function validateRemote(root) {
  const repoRoot = root || getRoot();
  const dir = roadmapDir(repoRoot);
  const issues = readRoadmapIssues(dir);
  const drift = [];
  for (const it of issues) {
    if (String(it.status || '').toLowerCase() !== 'open') continue;
    const n = parseInt(String(it.issue).replace('#', ''), 10);
    if (!Number.isInteger(n) || n <= 0) continue;
    if (issueIsClosed(n)) drift.push(n);
  }
  return drift;
}

function cmdValidateRemote() {
  if (process.env.KAOLA_WORKFLOW_OFFLINE === '1') {
    process.stdout.write('skipped: offline\n');
    return;
  }
  const drift = validateRemote(getRoot());
  if (drift.length > 0) {
    process.stderr.write(
      'roadmap drift: ' + drift.map(n => 'issue-' + n + '.md').join(', ') +
      ' marked open but closed on remote; run finalize or remove stale .roadmap files\n'
    );
    process.exitCode = 1;
    return;
  }
  process.stdout.write('ok\n');
}

function cmdInitIssue(argv) {
  const args = parseArgs(argv);
  const n = Number(args['issue']);
  if (!Number.isInteger(n) || n <= 0) {
    process.stderr.write('--issue must be a positive integer\n');
    process.exitCode = 1;
    return;
  }
  const title = (args['title'] || '—').replace(/[\r\n]/g, ' ');
  const status = (args['status'] || 'open').replace(/[\r\n]/g, ' ');
  const workflowProject = (args['workflow-project'] || '—').replace(/[\r\n]/g, ' ');
  const nextStep = (args['next-step'] || '—').replace(/[\r\n]/g, ' ');
  const root = getRoot();
  const dir = roadmapDir(root);
  const filePath = path.join(dir, 'issue-' + n + '.md');
  const content = [
    'issue: #' + n,
    'title: ' + title,
    'status: ' + status,
    'workflow_project: ' + workflowProject,
    'next_step: ' + nextStep,
    '',
  ].join('\n');
  const created = createFileExclusive(filePath, content);
  process.stdout.write(created ? 'created: issue-' + n + '.md\n' : 'skip: issue-' + n + '.md already exists\n');
}

function cmdProjectName(argv) {
  const args = parseArgs(argv);
  const n = Number(args['issue']);
  if (!Number.isInteger(n) || n <= 0) {
    process.stderr.write('--issue must be a positive integer\n');
    process.exitCode = 1;
    return;
  }
  const root = getRoot();
  const filePath = path.join(roadmapDir(root), 'issue-' + n + '.md');
  let content;
  try {
    content = fs.readFileSync(filePath, 'utf8');
  } catch (_) {
    process.exitCode = 1;
    return;
  }
  const name = field(content, 'workflow_project').replace(/\|/g, '').trim();
  if (!name || name === '—') {
    process.exitCode = 1;
    return;
  }
  process.stdout.write(name + '\n');
}

function main() {
  const sub = process.argv[2];
  if (!sub || sub === 'generate') { cmdGenerate(); return; }
  if (sub === 'migrate') { cmdMigrate(); return; }
  if (sub === 'validate') { cmdValidate(); return; }
  if (sub === 'validate-remote') { cmdValidateRemote(); return; }
  if (sub === 'init-issue') { cmdInitIssue(process.argv.slice(3)); return; }
  if (sub === 'project-name') { cmdProjectName(process.argv.slice(3)); return; }
  throw new Error('Unknown subcommand: ' + sub + '. Use generate, migrate, validate, validate-remote, init-issue, or project-name.');
}

module.exports = {
  cmdGenerate,
  regenerateRoadmap,
  validateRemote,
  cmdValidateRemote,
  readRoadmapIssues,
  roadmapDir,
  buildRoadmapContent,
};

if (require.main === module) {
  try {
    main();
  } catch (err) {
    process.stderr.write(err.message + '\n');
    process.exitCode = 1;
  }
}
