#!/usr/bin/env node
'use strict';

const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');
const forge = require('./kaola-gitlab-forge');

function assert(cond, msg) { if (!cond) throw new Error(msg); }

function field(content, name) {
  const escaped = name.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const match = content.match(new RegExp('^' + escaped + ':[ \\t]*(.+)$', 'm'));
  return match ? match[1].trim() : '';
}

function getRoot() {
  try {
    return execFileSync('git', ['rev-parse', '--show-toplevel'], {
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore']
    }).trim();
  } catch (_) {
    return process.cwd();
  }
}

function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i++) {
    if (!argv[i].startsWith('--')) continue;
    const key = argv[i].slice(2);
    if (argv[i + 1] !== undefined && !argv[i + 1].startsWith('--')) {
      args[key] = argv[++i];
    } else {
      args[key] = true;
    }
  }
  return args;
}

const RULES_BLOCK = `
## Rules

- GitLab issues are the source of truth when available.
- GitLab issue \`iid\` maps to workflow issue number.
- Move completed workflow project folders to \`kaola-workflow/archive/\`.
- Close linked GitLab issues only after acceptance criteria pass.`;

const HEADER = '<!-- generated by plugins/kaola-workflow-gitlab/scripts/kaola-gitlab-workflow-roadmap.js - do not edit -->\n' +
  '# Kaola-Workflow GitLab Roadmap\n\n' +
  'This file mirrors active unfinished GitLab work.\n\n' +
  '## Active Work\n\n' +
  '| Issue | Title | Status | Workflow Project | Next Step |\n' +
  '|-------|-------|--------|------------------|-----------|';

function roadmapDir(root) {
  return path.join(root, 'kaola-workflow', '.roadmap');
}

function roadmapFile(root) {
  return path.join(root, 'kaola-workflow', 'ROADMAP.md');
}

function readRoadmapIssues(dir) {
  if (!fs.existsSync(dir)) return [];
  const files = fs.readdirSync(dir)
    .filter(file => /^issue-\d+\.md$/.test(file))
    .sort((a, b) => parseInt(a.match(/\d+/)[0], 10) - parseInt(b.match(/\d+/)[0], 10));
  return files.map(file => {
    const content = fs.readFileSync(path.join(dir, file), 'utf8');
    return {
      issue: field(content, 'issue'),
      title: field(content, 'title') || '—',
      status: field(content, 'status') || 'open',
      workflow_project: field(content, 'workflow_project') || '—',
      next_step: field(content, 'next_step') || '—'
    };
  }).filter(data => data.issue && /^#\d+$/.test(data.issue));
}

function buildTableRow(data) {
  const cols = [
    data.issue,
    (data.title || '—').replace(/\|/g, '\\|'),
    data.status || 'open',
    (data.workflow_project || '—').replace(/\|/g, '\\|'),
    (data.next_step || '—').replace(/\|/g, '\\|')
  ];
  return '| ' + cols.join(' | ') + ' |';
}

function buildRoadmapContent(issues) {
  const rows = issues.length > 0 ? issues.map(buildTableRow) : ['| none | No active work | — | — | — |'];
  return HEADER + '\n' + rows.join('\n') + '\n' + RULES_BLOCK + '\n';
}

function isGeneratedRoadmap(content) {
  return content.startsWith('<!-- generated by plugins/kaola-workflow-gitlab/scripts/kaola-gitlab-workflow-roadmap.js');
}

function parseRoadmapTable(text) {
  const match = text.match(/## Active Work\n[\s\S]*?(?=\n## |\n$|$)/);
  if (!match) return [];
  const rows = [];
  const rowPattern = /^\| #(\d+) \| ([^|]+?) \| ([^|]+?) \| ([^|]+?) \| ([^|]+?) \|$/gm;
  let row;
  while ((row = rowPattern.exec(match[0])) !== null) {
    rows.push({
      issue: '#' + row[1].trim(),
      title: row[2].trim(),
      status: row[3].trim(),
      workflow_project: row[4].trim(),
      next_step: row[5].trim()
    });
  }
  return rows;
}

function guardAgainstMissingRoadmapSource(dir, outFile) {
  if (fs.existsSync(dir) || !fs.existsSync(outFile)) return;
  const existing = fs.readFileSync(outFile, 'utf8');
  if (!isGeneratedRoadmap(existing)) return;
  const activeRows = parseRoadmapTable(existing);
  if (activeRows.length === 0) return;
  throw new Error(
    'Refusing to replace non-empty generated ROADMAP.md with no active work because ' +
    'kaola-workflow/.roadmap is missing. Restore .roadmap or recreate per-issue source files before generate.'
  );
}

function writeFileAtomicReplace(filePath, content) {
  let existing = '';
  try { existing = fs.readFileSync(filePath, 'utf8'); } catch (_) {}
  if (existing === content) return false;
  const dir = path.dirname(filePath);
  fs.mkdirSync(dir, { recursive: true });
  const tmp = path.join(
    dir,
    '.' + path.basename(filePath) + '.' + process.pid + '.' + Date.now() + '.' +
      Math.random().toString(16).slice(2) + '.tmp'
  );
  let fd;
  try {
    fd = fs.openSync(tmp, 'wx');
    fs.writeFileSync(fd, content, 'utf8');
    fs.fsyncSync(fd);
    fs.closeSync(fd);
    fd = undefined;
    fs.renameSync(tmp, filePath);
  } catch (err) {
    if (fd !== undefined) {
      try { fs.closeSync(fd); } catch (_) {}
    }
    try { fs.unlinkSync(tmp); } catch (_) {}
    throw err;
  }
  return true;
}

function createFileExclusive(filePath, content) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  let fd;
  let created = false;
  try {
    fd = fs.openSync(filePath, 'wx');
    created = true;
    fs.writeFileSync(fd, content, 'utf8');
    fs.fsyncSync(fd);
    fs.closeSync(fd);
    fd = undefined;
    return true;
  } catch (err) {
    if (fd !== undefined) {
      try { fs.closeSync(fd); } catch (_) {}
    }
    if (err && err.code === 'EEXIST') return false;
    if (created) {
      try { fs.unlinkSync(filePath); } catch (_) {}
    }
    throw err;
  }
}

function issueRecordContent(issueIid, title, status, workflowProject, nextStep, labels, url) {
  return [
    'issue: #' + issueIid,
    'title: ' + (title || '—').replace(/[\r\n]/g, ' '),
    'status: ' + (status || 'open').replace(/[\r\n]/g, ' '),
    'workflow_project: ' + (workflowProject || 'issue-' + issueIid).replace(/[\r\n]/g, ' '),
    'next_step: ' + (nextStep || url || 'ready').replace(/[\r\n]/g, ' '),
    'labels: ' + (labels || []).join(', '),
    'url: ' + (url || ''),
    ''
  ].join('\n');
}

function writeIssueRecord(root, issue, status, workflowProject, nextStep) {
  const issueIid = Number(issue.issue_iid || issue.number);
  if (!Number.isInteger(issueIid) || issueIid <= 0) return false;
  const filePath = path.join(roadmapDir(root), 'issue-' + issueIid + '.md');
  const url = issue.web_url || issue.url || '';
  const content = issueRecordContent(
    issueIid,
    issue.title || '—',
    status || issue.state || 'open',
    workflowProject || 'issue-' + issueIid,
    nextStep || url || 'ready',
    issue.labels || [],
    url
  );
  return writeFileAtomicReplace(filePath, content);
}

function refreshFromGitLab(root, options) {
  const opts = options || {};
  const issues = forge.listIssues({ perPage: opts.perPage || 100, state: opts.state || 'opened' });
  let wrote = 0;
  for (const issue of issues) {
    if (issue.state === 'closed') continue;
    if (writeIssueRecord(root, issue, issue.state || 'open', opts.workflowProject, issue.web_url || issue.url || 'ready')) wrote++;
  }
  const generated = writeFileAtomicReplace(roadmapFile(root), buildRoadmapContent(readRoadmapIssues(roadmapDir(root))));
  return { issues: issues.length, wrote, generated };
}

function regenerateRoadmap(root) {
  const repoRoot = root || getRoot();
  const dir = roadmapDir(repoRoot);
  const outFile = roadmapFile(repoRoot);
  guardAgainstMissingRoadmapSource(dir, outFile);
  const issues = readRoadmapIssues(dir);
  const content = buildRoadmapContent(issues);
  const wrote = writeFileAtomicReplace(outFile, content);
  return wrote ? 'generated' : 'up-to-date';
}
function cmdGenerate() {
  process.stdout.write(regenerateRoadmap(getRoot()) + '\n');
}

function cmdValidate() {
  const root = getRoot();
  const expected = buildRoadmapContent(readRoadmapIssues(roadmapDir(root)));
  let actual = '';
  try { actual = fs.readFileSync(roadmapFile(root), 'utf8'); } catch (_) {}
  if (actual !== expected) {
    process.stderr.write('ROADMAP.md is stale; run: node plugins/kaola-workflow-gitlab/scripts/kaola-gitlab-workflow-roadmap.js generate\n');
    process.exitCode = 1;
  } else {
    process.stdout.write('ok\n');
  }
}

function issueIsClosed(issueIid) {
  try {
    return forge.viewIssue(issueIid).state === 'closed';
  } catch (_) {
    return false;
  }
}

function validateRemote(root) {
  const repoRoot = root || getRoot();
  const issues = readRoadmapIssues(roadmapDir(repoRoot));
  const drift = [];
  for (const it of issues) {
    if (String(it.status || '').toLowerCase() !== 'open') continue;
    const issueIid = parseInt(String(it.issue).replace('#', ''), 10);
    if (!Number.isInteger(issueIid) || issueIid <= 0) continue;
    if (issueIsClosed(issueIid)) drift.push(issueIid);
  }
  return drift;
}

function cmdValidateRemote() {
  if (process.env.KAOLA_WORKFLOW_OFFLINE === '1') {
    process.stdout.write('skipped: offline\n');
    return;
  }
  const drift = validateRemote(getRoot());
  if (drift.length > 0) {
    process.stderr.write(
      'roadmap drift: ' + drift.map(n => 'issue-' + n + '.md').join(', ') +
      ' marked open but closed on remote; run finalize or remove stale .roadmap files\n'
    );
    process.exitCode = 1;
    return;
  }
  process.stdout.write('ok\n');
}

function cmdInitIssue(argv) {
  const args = parseArgs(argv);
  const issueIid = Number(args.issue);
  if (!Number.isInteger(issueIid) || issueIid <= 0) {
    process.stderr.write('--issue must be a positive integer\n');
    process.exitCode = 1;
    return;
  }
  const root = getRoot();
  const filePath = path.join(roadmapDir(root), 'issue-' + issueIid + '.md');
  const content = issueRecordContent(
    issueIid,
    args.title || '—',
    args.status || 'open',
    args['workflow-project'] || '—',
    args['next-step'] || '—',
    [],
    args.url || ''
  );
  if (createFileExclusive(filePath, content)) {
    process.stdout.write('created: issue-' + issueIid + '.md\n');
    return;
  }
  if (!args.update) {
    process.stdout.write('skip: issue-' + issueIid + '.md already exists\n');
    return;
  }
  const wrote = writeFileAtomicReplace(filePath, content);
  process.stdout.write(wrote ? 'updated: issue-' + issueIid + '.md\n' : 'skip: issue-' + issueIid + '.md already up-to-date\n');
}

function cmdProjectName(argv) {
  const args = parseArgs(argv);
  const issueIid = Number(args.issue);
  assert(Number.isInteger(issueIid) && issueIid > 0, '--issue must be a positive integer');
  const filePath = path.join(roadmapDir(getRoot()), 'issue-' + issueIid + '.md');
  const name = field(fs.readFileSync(filePath, 'utf8'), 'workflow_project').replace(/\|/g, '').trim();
  if (!name || name === '—') { process.exitCode = 1; return; }
  process.stdout.write(name + '\n');
}

function cmdRefresh(argv) {
  process.stdout.write(JSON.stringify(refreshFromGitLab(getRoot(), parseArgs(argv))) + '\n');
}

function main() {
  const sub = process.argv[2];
  if (!sub || sub === 'generate') { cmdGenerate(); return; }
  if (sub === 'refresh') { cmdRefresh(process.argv.slice(3)); return; }
  if (sub === 'validate') { cmdValidate(); return; }
  if (sub === 'validate-remote') { cmdValidateRemote(); return; }
  if (sub === 'init-issue') { cmdInitIssue(process.argv.slice(3)); return; }
  if (sub === 'project-name') { cmdProjectName(process.argv.slice(3)); return; }
  throw new Error('Unknown subcommand: ' + sub + '. Use generate, refresh, validate, validate-remote, init-issue, or project-name.');
}

if (require.main === module) {
  try { main(); } catch (err) { process.stderr.write(err.message + '\n'); process.exitCode = 1; }
}

module.exports = {
  buildRoadmapContent,
  createFileExclusive,
  guardAgainstMissingRoadmapSource,
  readRoadmapIssues,
  refreshFromGitLab,
  regenerateRoadmap,
  validateRemote,
  cmdValidateRemote,
  writeFileAtomicReplace,
  writeIssueRecord
};
